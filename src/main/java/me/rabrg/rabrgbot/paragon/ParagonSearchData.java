package me.rabrg.rabrgbot.paragon;

public class ParagonSearchData {

    private final int id;
    private final String name;

    public ParagonSearchData(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
